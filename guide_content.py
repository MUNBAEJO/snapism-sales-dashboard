def render_guide(key: str):
    """페이지 상단에 해당 대시보드 설명서를 펼침(expander)으로 렌더링."""
    body = _GUIDES.get(key)
    if not body:
        return
    with st.expander("📖 이 대시보드 설명서 — 처음이신가요? (클릭해서 펼치기)", expanded=False):
        st.markdown(_COMMON + body)
